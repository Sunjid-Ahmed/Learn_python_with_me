#slicing in lists
marks=[94,95,96,97,98,99]
print(marks[0:3])
print(marks[ :3])
print(marks[1:])
print(marks[ : ])
print(len(marks))

#negative index(slicing in lists)

marks1=[94,95,96,97,98,99] 
print(marks1[-6:-3]) #[-6:-3] means index -6 to -4
print(marks1[:])
print(marks1[-6:-1]) #[-6:-1] means index -6 to -2
print(len(marks1))